
    class MiniChat {
        void Ola() {
            System.out.println("Olá! Como posso ajudá-lo hoje?");
        }

        void ajuda() {
            System.out.println("Estou aqui para ajudar! O que você gostaria de saber?");
        }

        void comoEsta() {
            System.out.println("Estou bem, obrigado! E você?");
        }

        void Videgame() {
            System.out.println("Sim, eu gosto de jogos de videogame! Quais são os seus favoritos?");
        }

        void Esportes() {
            System.out.println("Sim, eu gosto de esportes! Qual é o seu esporte favorito?");
        }

        void quemsou() {
            System.out.println("Eu sou um chatbot criado para ajudá-lo.");
        }

        void Sair() {
            System.out.println("Até logo! Espero te ver novamente.");
        }

        void NaoEntendi() {
            System.out.println("Desculpe, não entendi. Pode repetir?");
        }
    }

